	/**
	 * In Java, int is a primitive data type whereas Integer is a wrapper class.
	 * 
	 * int, being a primitive data type has got less flexibility. We can only store
	 * the binary value of an integer in it.
	 * 
	 * Since Integer is a wrapper class for int data type, it gives us more
	 * flexibility in storing, converting and manipulating an int data.
	 * 
	 * Integer is a class and thus it can call various in-built methods defined in
	 * the class. Variables of type Integer store references to Integer objects,
	 * just as with any other reference (object) type.
	 * 
	 * 
	 * Note that every primitive type in Java has an equivalent wrapper class:
	 * 
	 * byte has Byte 
	 * short has Short 
	 * int has Integer 
	 * long has Long 
	 * boolean has Boolean 
	 * char has Character 
	 * float has Float 
	 * double has Double
	 * 
	 */

